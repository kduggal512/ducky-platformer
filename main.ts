function levelSwitch () {
    if (level == 1) {
        setScene()
        tiles.placeOnTile(mySprite, tiles.getTileLocation(0, 14))
    } else if (level == 2) {
        setScene()
        tiles.placeOnTile(mySprite, tiles.getTileLocation(0, 12))
    } else if (level == 3) {
        setScene()
        tiles.placeOnTile(mySprite, tiles.getTileLocation(0, 14))
    } else {
        setScene()
        tiles.placeOnTile(mySprite, tiles.getTileLocation(0, 10))
    }
    levelSetup()
}
